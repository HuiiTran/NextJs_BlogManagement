import mongoose from "mongoose";
const PostSchema = new mongoose.Schema({
    userId: {
        type: String,
        required: true,
    },
    content: {
        type: String,
        required: true,
    },
    title: {
        type: String,
        required: true,
        unique: true,
    },
    category: {
      type: String,
      default: 'uncategorized',
    },
    slug: {
      type: String,
      required: true,
      unique: true,
    },
},
    { timestamps: true }
);
const Post = mongoose.models.Post || mongoose.model('Post', PostSchema);
export default Post;